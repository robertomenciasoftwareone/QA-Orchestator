/**
 * GherkinGenerator
 * Generates Gherkin BDD feature files from Jira stories.
 * Compatible with Hercules (testzeus-hercules) execution format.
 */
export class GherkinGenerator {
  generate(story, confluencePage, options = {}) {
    const fields = story.fields || {};
    const summary = fields.summary || "Feature";
    const description = this._extractText(fields.description);
    const acText = confluencePage?.text || "";

    const lines = [];
    lines.push(`# Generated by qa-orchestrator-mcp`);
    lines.push(`# Story: ${story.key} - ${summary}`);
    lines.push(`# Date: ${new Date().toISOString().split("T")[0]}`);
    lines.push(``);
    lines.push(`Feature: ${summary}`);
    lines.push(``);
    lines.push(`  # ${description.split("\n")[0] || summary}`);
    lines.push(``);

    // â”€â”€ Main Flow scenario â”€â”€
    lines.push(`  @MF @TC_GENERATED_BY_IA @POR_REVISAR`);
    lines.push(`  Scenario: [MF] Flujo principal - ${summary}`);
    lines.push(`    Given el usuario estĂ¡ autenticado en el sistema`);
    lines.push(`    And el usuario tiene los permisos necesarios para "${summary}"`);
    lines.push(`    When el usuario navega a la secciĂ³n correspondiente`);
    lines.push(`    And el usuario completa el flujo principal con datos vĂ¡lidos`);
    lines.push(`    Then el sistema procesa la solicitud correctamente`);
    lines.push(`    And el sistema muestra un mensaje de confirmaciĂ³n`);
    lines.push(`    And el resultado es conforme a los criterios de aceptaciĂ³n`);
    lines.push(``);

    // â”€â”€ Alternative flow scenario â”€â”€
    lines.push(`  @AF @TC_GENERATED_BY_IA @POR_REVISAR`);
    lines.push(`  Scenario: [AF] Flujo alternativo - Cancelar operaciĂ³n en "${summary}"`);
    lines.push(`    Given el usuario estĂ¡ autenticado en el sistema`);
    lines.push(`    And el usuario estĂ¡ en el flujo de "${summary}"`);
    lines.push(`    When el usuario inicia la operaciĂ³n principal`);
    lines.push(`    And el usuario decide cancelar antes de completar`);
    lines.push(`    Then el sistema no guarda los cambios parciales`);
    lines.push(`    And el usuario regresa al estado anterior`);
    lines.push(``);

    // â”€â”€ Exception flow scenario: required fields â”€â”€
    lines.push(`  @EX @TC_GENERATED_BY_IA @POR_REVISAR`);
    lines.push(`  Scenario: [EX] ExcepciĂ³n - Campos obligatorios vacĂ­os en "${summary}"`);
    lines.push(`    Given el usuario estĂ¡ autenticado en el sistema`);
    lines.push(`    And el usuario estĂ¡ en el formulario de "${summary}"`);
    lines.push(`    When el usuario intenta completar la operaciĂ³n sin rellenar campos obligatorios`);
    lines.push(`    Then el sistema muestra mensajes de error de validaciĂ³n`);
    lines.push(`    And el sistema no permite continuar hasta corregir los errores`);
    lines.push(``);

    // â”€â”€ Exception flow scenario: invalid data â”€â”€
    lines.push(`  @EX @TC_GENERATED_BY_IA @POR_REVISAR`);
    lines.push(`  Scenario: [EX] ExcepciĂ³n - Datos invĂ¡lidos en "${summary}"`);
    lines.push(`    Given el usuario estĂ¡ autenticado en el sistema`);
    lines.push(`    And el usuario estĂ¡ en el formulario de "${summary}"`);
    lines.push(`    When el usuario introduce datos con formato invĂ¡lido`);
    lines.push(`      | campo       | valor_invalido        |`);
    lines.push(`      | email       | esto-no-es-un-email   |`);
    lines.push(`      | telefono    | abc                   |`);
    lines.push(`      | fecha       | 99/99/9999            |`);
    lines.push(`    Then el sistema muestra errores de formato para cada campo invĂ¡lido`);
    lines.push(`    And los datos no se guardan hasta que sean correctos`);
    lines.push(``);

    if (options.includeEdgeCases) {
      lines.push(`  @AF @TC_GENERATED_BY_IA @POR_REVISAR`);
      lines.push(`  Scenario: [AF] Flujo alternativo - Datos mĂ­nimos en "${summary}"`);
      lines.push(`    Given el usuario estĂ¡ autenticado en el sistema`);
      lines.push(`    And el usuario estĂ¡ en el formulario de "${summary}"`);
      lines.push(`    When el usuario rellena Ăºnicamente los campos obligatorios`);
      lines.push(`    And el usuario deja todos los campos opcionales vacĂ­os`);
      lines.push(`    And el usuario confirma la operaciĂ³n`);
      lines.push(`    Then el sistema acepta la operaciĂ³n con datos mĂ­nimos`);
      lines.push(`    And el resultado se crea correctamente`);
      lines.push(``);
    }

    // â”€â”€ Scenario Outline for parametrized data (if AC detected) â”€â”€
    const acLines = acText.split("\n").filter((l) => l.trim().startsWith("-") || l.trim().startsWith("*")).slice(0, 3);
    if (acLines.length > 0) {
      lines.push(`  @MF @TC_GENERATED_BY_IA @POR_REVISAR`);
      lines.push(`  Scenario Outline: [MF] Verificar criterios de aceptaciĂ³n - ${summary}`);
      lines.push(`    Given el usuario estĂ¡ autenticado en el sistema`);
      lines.push(`    When el usuario ejecuta la acciĂ³n "<accion>"`);
      lines.push(`    Then el sistema muestra "<resultado_esperado>"`);
      lines.push(``);
      lines.push(`    Examples:`);
      lines.push(`      | accion                          | resultado_esperado                    |`);
      for (const ac of acLines) {
        const clean = ac.replace(/^[-*]\s*/, "").trim().substring(0, 30).padEnd(30);
        lines.push(`      | ${clean} | El sistema cumple el criterio         |`);
      }
      lines.push(``);
    }

    return lines.join("\n");
  }

  _extractText(node) {
    if (!node) return "";
    if (typeof node === "string") return node;
    if (node.type === "text") return node.text || "";
    if (Array.isArray(node)) return node.map((n) => this._extractText(n)).join(" ");
    if (node.content) return this._extractText(node.content);
    return "";
  }
}
